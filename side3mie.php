        <h2 id="ophav">Ophavsret</h2>
        <p id=ophav1> Ophavsret i Danmark er en lovgivning som er med til at sikre mod tyveri af både digitale og fysiske medier det vil sige at man ikke må kopiere et billede, en tekst et design eller en video uden tilladelse fra ophavsretshaveren.(kilde: http://www.jura-guide.dk/ophavsret)  <br>
        Det som der indebærer at man kan få ophavsret på noget, er at det bære præg af originalitet samt det skal være skabt fra bunden af, af skaberen(kilde: http://www.jura-guide.dk/ophavsret  krav ved ophavsret).<br>
        Billeder som er taget med et kamera, en tekst som er skrevet fra bunden, vil altid være bundet af ophavsretten også selv om skaberen ikke gør opmærksom på det ved f.eks. at sætte Copyright-symbolet på deres kreationer eller på hjemmesiden de ligger(kilde: http://www.jura-guide.dk/ophavsret  krav ved ophavsret).</p>
        <p id="ophav2">Selvom der er generelle regler hvad angår ophavsret, <br> så er der med den udvikling som der er sket digitalt over de seneste årtier.<br> Så der er udformet regler for ophavsret for socialemedier </p>
